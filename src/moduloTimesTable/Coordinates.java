package moduloTimesTable;

/**
 * Class to store x,y coordinates.
 * Established for use in drawing dots and
 * lines. No important functions besides getters
 * and setters
 */
public class Coordinates {
    private float x;
    private float y;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
